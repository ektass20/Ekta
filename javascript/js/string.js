//LOOPS AND STRINGS IN JS//
 
//loops: loops are used to execute a piece of code again and again//

//their are for,do,do while loops, for being the mostly used//

for (let i=1; i<=5; i++){
    console.log("i = ", i);
}
//variables accessed should be only inside the loops//

//While loop: to do some work.//

let i = 1;
while(i<=5){
    console.log("i=",i);
    i++;
}